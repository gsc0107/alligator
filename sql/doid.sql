drop schema DISEASEONTOLOGY RESTRICT;
create schema DISEASEONTOLOGY;
set schema DISEASEONTOLOGY;

drop INDEX DOID_TERMIDX1;
drop INDEX DOID_TERM2CHILDIDX1;
drop INDEX DOID_TERM2CHILDIDX2;
drop INDEX DOID_TERM2CHILDIDX3;
ALTER TABLE TERM2CHILD DROP CONSTRAINT DOID_TERM2CHILD_FK1;
ALTER TABLE TERM2CHILD DROP CONSTRAINT DOID_TERM2CHILD_FK2;


drop table TERM;
create table TERM
	(
	id INT not null primary key , --GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),  
	uri varchar(60) not null,
	content clob
	);

create UNIQUE index DOID_TERMIDX1  ON TERM(uri);



drop table TERM2CHILD;
create table TERM2CHILD
	(
	id INT not null primary key, -- GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1),  
	parent_id INT not null  ,
	child_id INT not null  ,
	CONSTRAINT DOID_TERM2CHILD_FK1 FOREIGN KEY(parent_id) REFERENCES TERM(id),
	CONSTRAINT DOID_TERM2CHILD_FK2 FOREIGN KEY(child_id) REFERENCES TERM(id)
	);
delete from TERM2CHILD;



create UNIQUE index DOID_TERM2CHILDIDX1  ON TERM2CHILD(parent_id,child_id);
create index DOID_TERM2CHILDIDX2  ON TERM2CHILD(parent_id);
create index DOID_TERM2CHILDIDX3  ON TERM2CHILD(child_id);





